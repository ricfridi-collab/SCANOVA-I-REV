function generateExam() {
    const sub = document.getElementById('subjectSelect').value;
    const cont = document.getElementById('examContainer');
    const evalArea = document.getElementById('evaluationArea');
    evalArea.style.display = 'none';

    if(!questionBank[sub]) return;

    // خلط عشوائي واختيار 4 أسئلة
    const qList = questionBank[sub].sort(() => 0.5 - Math.random()).slice(0, 4);

    let html = `<div class="exam-paper">
        <h2 style="text-align:center; text-decoration:underline; margin-bottom:20px;">امتحان: ${getSubName(sub)}</h2>`;
    
    qList.forEach((item, i) => {
        html += `
        <div class="q-block">
            <h4>السؤال ${i+1}:</h4>
            <p>${item.q}</p>
            <small>تلميح: ${item.h}</small>
            <div class="ans-box" contenteditable="true"></div>
        </div>`;
    });

    html += `</div><div style="text-align:center; margin-top:10px;">
        <button class="btn-main" onclick="finishExam()">إنهاء وتقييم</button>
    </div>`;

    cont.innerHTML = html;
    cont.style.opacity = 0;
    setTimeout(() => cont.style.opacity = 1, 50);
}

function getSubName(key) {
    const names = {math:'الرياضيات', science:'العلوم', physics:'الفيزياء', islamic:'العلوم الإسلامية', civic:'التربية المدنية', french:'الفرنسية', english:'الإنجليزية', arabic:'العربية', history:'الاجتماعيات', geography: 'الجغرافيا'};
    return names[key];
}

function finishExam() {
    document.getElementById('evaluationArea').style.display = 'block';
    document.getElementById('evaluationArea').scrollIntoView({behavior:'smooth'});
}

function saveEval() {
    const n = document.getElementById('notes').value;
    if(n.trim() === "") { 
        showToast("الرجاء كتابة ملاحظة أولاً"); 
        return; 
    }
    showToast("تم حفظ تقريرك بنجاح! أنت مبدع.");
    document.getElementById('notes').value = "";
    document.getElementById('evaluationArea').style.display = 'none';
}
